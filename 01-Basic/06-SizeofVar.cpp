#include<iostream>
using namespace std;

int main() {

    int a = 5;
    cout<<"Size of Int: "<<sizeof(a)<<endl;

    char ch = 5;
    cout<<"Size of char: "<<sizeof(ch)<<endl;

    bool flag = 1;
    cout<<"Size of bool: "<<sizeof(flag)<<endl;

    double db = 1;
    cout<<"Size of double: "<<sizeof(db)<<endl;

    float f = 1;
    cout<<"Size of float: "<<sizeof(f)<<endl;

    long l = 1;
    cout<<"Size of long: "<<sizeof(l)<<endl;

    long ll = 1;
    cout<<"Size of long long: "<<sizeof(ll)<<endl;

    return 0;
}